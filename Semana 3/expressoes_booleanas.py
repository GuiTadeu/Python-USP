# Expressões usando AND
fizerSol = True
forFeriado = False
vouParaPraia = fizerSol and forFeriado
print('Vou para praia?', vouParaPraia)

# Expressões usando o OR
paitrocinio = False
rolarPromocao = True
vouAoShow = paitrocinio or rolarPromocao
print('Vou para o show?', vouAoShow)

